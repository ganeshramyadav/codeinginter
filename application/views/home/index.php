      function initAutocomplete() {
    	autocomplete = new google.maps.places.Autocomplete(
    		(document.getElementById('autocomplete')),
    		{types: ['geocode'],
    		componentRestrictions: {country: 'us'}
    		});
    	autocomplete.addListener('place_changed', fillInAddress);
