    	  var addressType = place.address_components[i].types[0];
    	  if (componentForm[addressType]) {
    		var val = place.address_components[i][componentForm[addressType]];
    		full_address[addressType] = val;
    		//console.log(addressType + ' - ' + val);
    	  }
    	}
